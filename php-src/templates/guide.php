<p class='steps'>
    Step 2 -  Definition of the regional context, vision, priorities and indicators
</p>   
<p>
    In the second step of the application, the user will be able to complete all 
    relevant information that he wants to include to the specific intervention 
    logic diagram <strong>(Fig. 3)</strong>. The information will be based on the outcomes of 
    methodologies that have been implemented during previous phases, and will 
    be grouped into four main categories:
</p>
<ol style='list-style-type: decimal'>
	<li><i>Context</i>: this part includes information coming from the analysis of the regional context, related to the specific Investment Priority.</li>
	<li><i>Vision / Priorities</i>: this part included information related to the vision and priorities’ setting phases. In order to better organise the input four sub-categories have been included, related to specific methodologies that are being used in these phases. These include: EDP, extroversion analysis, related variety and foresight. Not all of them should be completed during an intervention logic design process.</li>
	<li><i>Policy mix</i>: Definition of actions and action plan implementation are the two main sub-groups of the policy mix part. Both of them focus on the definition of the groups of categories of actions and regulatory instruments of intervention that could be used to support the specific Investment Priority. 
	</li>
	<li><i>Monitoring</i>: : the last part of the intervention logic diagram includes the presentation of the selected output and result indicators related to the specific Investment Priority. As it can be seen, output indicators should be related to the definition of actions, as input for them will be gathered through the implemented actions. 
	</li>
</ol>

<p>
    Moreover, result indicators are related to Vision and Priorities, trying to 
    capture the more general targets of the region, through the reinforcement of 
    the specific Investment Priority.
</p>

